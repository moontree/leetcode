class Solution(object):
    def sampleStats(self, count):
        """
        :type count: List[int]
        :rtype: List[float]
        """
        min_val, max_val, mean_val, median_val, mode, max_count = 257., 0., 0., 0., 0., 0
        s = 0
        total_count = 0
        counts = []
        for i, c in enumerate(count):
            s += i * c
            total_count += c
            if c > 0:
                min_val = min(min_val, i)
                max_val = max(max_val, i)
            counts.append(total_count)
            if max_count < c:
                max_count, mode = c, i
        mil, mir = total_count / 2, total_count / 2 + 1
        i1, i2 = -1, -1
        for i, c in enumerate(counts):
            if i1 == -1 and mil <= c:
                i1 = i
            if i2 == -1 and mir <= c:
                i2 = i
                break
        if total_count % 2 == 1:
            median_val = i1
        else:
            median_val = (i1 + i2) / 2.
        return [min_val, max_val, s * 1.0 / total_count, median_val, mode]


